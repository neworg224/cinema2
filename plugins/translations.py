def get_translation(key: str, lang_code: str = 'en') -> str:
    """
    Get translation for a given key in the specified language
    :param key: The translation key to look up
    :param lang_code: Language code (defaults to 'en')
    :return: Translated string
    """
    # If language doesn't exist, fallback to English
    if lang_code not in TRANSLATIONS:
        lang_code = 'en'
    
    # If key doesn't exist in the language, fallback to English version
    translation = TRANSLATIONS[lang_code].get(key, TRANSLATIONS['en'].get(key, ''))
    return translation

def get_translated_message(message_key: str, language: str) -> str:
    """
    Get translated message for given key and language
    Falls back to English if translation not found
    """
    if language not in TRANSLATIONS:
        language = 'en'
    
    if message_key not in TRANSLATIONS[language]:
        # Fallback to English if message key not found in selected language
        return TRANSLATIONS['en'].get(message_key, message_key)
    
    return TRANSLATIONS[language][message_key]

